CREATE VIEW VTrackListing AS
  SELECT A.album, A.song, A.writer, B.singer
  FROM
    (SELECT albums.title AS album, songs.title AS song, group_concat(people.lname) AS writer
    FROM albums, songs, people, wroteMusic, songsOnAlbums
    WHERE albums.albumID == songsOnAlbums.albumID
      AND songsOnAlbums.songID == songs.songID
      AND songs.songID == wroteMusic.songID
      AND wroteMusic.personID == people.personID
    GROUP BY songs.songID) AS A
  LEFT JOIN
    (SELECT songs.title AS song, group_concat(people.lname) AS singer
    FROM songs, people, singsOn
    WHERE songs.songID == singsOn.songID
      AND singsOn.personID == people.personID
    GROUP BY songs.songID) AS B
  ON A.song == B.song;
COMMIT;
